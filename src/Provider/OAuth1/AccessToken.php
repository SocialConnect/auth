namespace SocialConnect\Auth\Provider\OAuth1;

class AccessToken
{
    /**
     * @var string
     */
    protected $token;

    public function __construct($token)
    {
        $this->token = $token;
    }

    /**
     * @return string
     */
    public function getToken()
    {
        return $this->token;
    }
}
